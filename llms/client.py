class AsyncLLMClient:
    def __init__(
        self,
        base_url: str = "https://localhost:8000/v1",
        api_key: str = "EMPTY",
        model: str = "Qwen/Qwen2.5-Coder-32B-Instruct",
        max_concurrent_requests: int = 20,
        max_retry: int = 3
    ):
        """
        初始化异步OpenAI客户端
        
        Args:
            base_url (str): API基础URL
            api_key (str): OpenAI API密钥
            model (str): 使用的模型名称
            max_concurrent_requests (int): 最大并发请求数
            max_retry (int): 最大重试次数
        """
        self.api_key = api_key
        self.base_url = base_url
        self.model = model
        self.max_retry = max_retry
        self.semaphore = asyncio.Semaphore(max_concurrent_requests)
        self.headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json"
        }
        # self.timeout = aiohttp.ClientTimeout(total=10)

    async def _send_request(
        self,
        session: aiohttp.ClientSession,
        endpoint: str,
        payload: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        发送单个异步请求
        
        Args:
            session (aiohttp.ClientSession): HTTP会话
            endpoint (str): API端点
            payload (Dict[str, Any]): 请求载荷
            
        Returns:
            Dict[str, Any]: API响应结果
        """
        if isinstance(self.base_url, ListConfig):
            base_url = random.choice(self.base_url)
        else:
            base_url = self.base_url
        url = f"{base_url}/{endpoint}"
        async with self.semaphore:
            async with session.post(url, json=payload, headers=self.headers) as response:
                response.raise_for_status()
                return await response.json()

    async def agenerate_batch(
        self,
        messages_list: List[List[Dict[str, str]]], # | List[Dict[str, str]],
        model: str = None,
        require_json: bool = False,
        task_desc: str = "llm batch calling",
        **kwargs
    ) -> List[Dict[str, Any]]:
        """
        并发处理多个聊天补全请求
        
        Args:
            messages_list (List[List[Dict[str, str]]]): 消息列表的列表，每个元素代表一个请求的messages
            model (str): 使用的模型
            require_json (bool): 是否要求JSON格式响应
            **kwargs: 其他可选参数（如temperature, max_tokens等）
            
        Returns:
            List[Dict[str, Any]]: 响应列表
        """
        if len(messages_list) and isinstance(messages_list[0], dict):
            messages_list = [messages_list]
        if require_json:
            kwargs["response_format"] = {"type": "json_object"}
        model = model if model else self.model
        async with aiohttp.ClientSession() as session:
            tasks = []
            for messages in messages_list:
                payload = {
                    "model": model,
                    "messages": messages,
                    **kwargs
                }
                task = asyncio.create_task(
                    self._send_request(session, "chat/completions", payload)
                )
                tasks.append(task)
            return await tqdm_asyncio.gather(*tasks, desc=task_desc)
    
    def generate(
        self,
        messages: List[Dict[str, str]],
        model: str = None,
        require_json: bool = False,
        **kwargs
    ) -> Dict[str, Any]:
        """
        处理单个聊天补全请求
        
        Args:
            messages (List[Dict[str, str]]): 消息列表，每个元素代表一个消息
            model (str): 使用的模型
            require_json (bool): 是否要求JSON格式响应
            **kwargs: 其他可选参数（如temperature, max_tokens等）
            
        Returns:
            Dict[str, Any]: 响应字典
        """
        if require_json:
            kwargs["response_format"] = {"type": "json_object"}

        model = model if model else self.model
        payload = {
            "model": model,
            "messages": messages,
            **kwargs
        }
        if isinstance(self.base_url, ListConfig):
            base_url = random.choice(self.base_url)
        else:
            base_url = self.base_url
        response = requests.post(
            f"{base_url}/chat/completions",
            headers=self.headers,
            json=payload
        )
        if response.status_code == 200:
            return response.json()
        else:
            raise Exception(f"请求失败，状态码: {response.status_code}, 响应内容: {response.text}")

    async def agenerate(
        self,
        messages: List[Dict[str, str]],
        model: str = None,
        require_json: bool = False,
        **kwargs
    ) -> Dict[str, Any]:
        """
        异步处理单个聊天补全请求
        
        Args:
            messages (List[Dict[str, str]]): 消息列表，每个元素代表一个消息
            model (str): 使用的模型
            require_json (bool): 是否要求JSON格式响应
            **kwargs: 其他可选参数（如temperature, max_tokens等）
            
        Returns:
            Dict[str, Any]: 响应字典
        """
        if require_json:
            kwargs["response_format"] = {"type": "json_object"}
        
        model = model if model else self.model
        payload = {
            "model": model,
            "messages": messages,
            **kwargs
        }

        cur_retry = 0
        async with aiohttp.ClientSession() as session:
            while cur_retry < self.max_retry:
                cur_retry += 1
                try:
                    response = await self._send_request(
                        session,
                        "chat/completions",
                        payload
                    )
                    ret_text = response['choices'][0]['message']['content'].strip()
                    if require_json:
                        try:
                            json.loads(ret_text)
                        except Exception as e:
                            payload["temperature"] = random.uniform(0.0, 0.1)
                            payload["messages"][-2]["content"]+= "\n请确保输出格式为json且json格式正确。"
                            logger.warning(f"Response is not a valid json: {str(e)},try ({cur_retry}/{self.max_retry})again")
                            logger.warning(str(e))
                            continue
                    return response
                except aiohttp.ClientError as e:
                    logger.error(f"Request failed: {str(e)}")
                    continue
                    raise Exception(f"Request failed: {str(e)}")
                except asyncio.TimeoutError as e:
                    last_exception = e
                    logger.warning(f"请求超时{str(e)},maybe the response is still generating but too slow or too long, or max_concurrent_requests is too large, causing packet loss")
                    continue
                except Exception as e:
                    # raise LLMRetry_Error(f"Unexpected error: {str(e)}")
                    raise Exception(f"Unexpected error: {str(e)}")
        logger.error(f"Max retry reached: {cur_retry}, but the return of llm is not valid json")
        raise LLMRetry_Error(f"Max retry reached: {cur_retry}, remove the element from the list")


